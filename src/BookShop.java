    private void txtaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtaddActionPerformed
        // TODO add your handling code here:
        
        String bname = txtbname.getText();
        int price = parseInt(txtprice.getText());
        int qty =  parseInt(txtqty.getValue().toString());
        int total = price * qty;
        
        table = (DefaultTableModel)tblProduct.getModel();
    
        int counter = table.getRowCount() + 1;
        table.addRow(new Object[]
        {
            counter, 
            txtbname.getText(),
            txtprice.getText(),
            txtqty.getValue().toString(),
            total
        }
        );
     
        int sum = 0;
        
        for(int i = 0; i < table.getRowCount(); i++){
             sum = sum + parseInt(table.getValueAt(i, 4).toString());
        }
       
        txttcost.setText(String.valueOf(sum));
        txtbcode.setText("");
        txtbname.setText("");
        txtprice.setText("");
        txtqty.setValue(0);
        txttcost.requestFocus();
        
    }//GEN-LAST:event_txtaddActionPerformed

    private void txtbcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbcodeKeyPressed
       if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           
           try {
               String bcode = txtbcode.getText();
               pst = con.prepareStatement("SELECT * FROM book WHERE bcode = ?");
               pst.setString(1, bcode);
               rs = pst.executeQuery();
               
               if(rs.next() == false){
                   JOptionPane.showMessageDialog(this,
                        "Book Code not found",
                        "Message",
                        JOptionPane.INFORMATION_MESSAGE);
               }else{
                   String bname = rs.getString("bname");
                   txtbname.setText(bname.trim());
                   
                   String price = rs.getString("price");
                   txtprice.setText(price.trim());
                   
                   txtqty.requestFocus();
               }
           } catch (SQLException ex) {
               Logger.getLogger(BookShop.class.getName()).log(Level.SEVERE, null, ex);
           }
            
       }
    }//GEN-LAST:event_txtbcodeKeyPressed

