     private void sales(){
         String tcost = txttcost.getText();
         String pay = txtpay.getText();
         String bal = txtbal.getText();
         
        try {
            String query = "INSERT INTO sales (subtotal, pay, balance) VALUES (?, ?, ?)";
            pst = con.prepareStatement( query, new String[] { "flight_id" });
            pst.setString(1, tcost);
            pst.setString(2, pay);
            pst.setString(3, bal);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            
            int lid = 0;
            if(rs.next()){
                lid = rs.getInt(1);
                //rs.getInt(1);
            }
            
            String query1 = "INSERT INTO sales_product (sales_id, bname, price, qty, total) VALUES (?, ?, ?, ?, ?)";
            pstmt = con.prepareStatement(query1);
            
            String bname = "";
            String price;
            String qty;
            int total = 0;
            
            for(int i = 0; i < tblProduct.getRowCount(); i++){
                bname = (String) tblProduct.getValueAt(i, 1);
                price = (String) tblProduct.getValueAt(i, 2);
                qty = (String) tblProduct.getValueAt(i, 3);
                total = (int) tblProduct.getValueAt(i, 4);
                pstmt.setInt(1, lid);
                pstmt.setString(2, bname);
                pstmt.setString(3, price);
                pstmt.setString(4, qty);
                pstmt.setInt(5, total);
                pstmt.executeUpdate();
                
            }
            
            JOptionPane.showMessageDialog(this,
                        "Sales Completed",
                        "Message",
                        JOptionPane.INFORMATION_MESSAGE);
            
            HashMap a = new HashMap();
            a.put("invo", lid);
            
             try {
                 JasperDesign jdesign = JRXmlLoader.load("D:\\Java Projects\\Book Shop\\src\\report1.jrxml"); //Change Me!
                 JasperReport jreport = JasperCompileManager.compileReport(jdesign);
                 JasperPrint jprint = JasperFillManager.fillReport(jreport, a, con);
                 JasperViewer.viewReport(jprint);
                 
             } catch (JRException ex) {
                 Logger.getLogger(BookShop.class.getName()).log(Level.SEVERE, null, ex);
             }
            
        } catch (SQLException ex) {
            Logger.getLogger(BookShop.class.getName()).log(Level.SEVERE, null, ex);
        }
         
     }
